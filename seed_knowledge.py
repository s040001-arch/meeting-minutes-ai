def main() -> None:
    sheet_id = os.getenv("KNOWLEDGE_SHEET_ID", "").strip()
    if not sheet_id:
        print("ERROR: KNOWLEDGE_SHEET_ID が設定されていません。.env または環境変数を確認してください。")
        sys.exit(1)

    print(f"対象スプレッドシート: {sheet_id}")
    existing = load_knowledge_memos()
    print(f"既存メモ数: {len(existing)}件")

    if existing:
        print("既存メモが存在します。以下の選択肢から選んでください：")
        print("  1: 既存を上書き（置き換え）")
        print("  2: 既存に追記（重複は自動除去）")
        print("  3: キャンセル")
        choice = input("選択 [1/2/3]: ").strip()
        if choice == "1":
            merged = SEED_MEMOS
        elif choice == "2":
            seen = set(existing)
            merged = list(existing) + [m for m in SEED_MEMOS if m not in seen]
        else:
            print("キャンセルしました。")
            return
    else:
        merged = SEED_MEMOS

    save_knowledge_memos(merged)
    print(f"完了: {len(merged)}件を書き込みました。")


if __name__ == "__main__":
    main()
